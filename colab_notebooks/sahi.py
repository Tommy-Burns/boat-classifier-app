patches = []
for feature in gj["features"]:
    coords = np.array(feature["geometry"]["coordinates"][0])

    if has_crs:
        # convert geo coords back to pixel for display
        with rasterio.open(image) as src:
            from rasterio.transform import rowcol
            rows, cols = rowcol(src.transform, coords[:, 0], coords[:, 1])
            pixel_coords = np.column_stack([cols, rows])
    else:
        # flip y back for display
        pixel_coords = np.column_stack([coords[:, 0], img_height - coords[:, 1]])

    polygon = Polygon(pixel_coords, closed=True)
    patches.append(polygon)

collection = PatchCollection(
    patches,
    facecolor="cyan",
    edgecolor="red",
    alpha=0.3,
    linewidths=1.5
)
ax.add_collection(collection)

ax.set_title(f"Boat Detections: {len(patches)} found", fontsize=16)
