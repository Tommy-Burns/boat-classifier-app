import json
import numpy as np
import cv2
import rasterio
from rasterio.transform import xy

class NumpyEncoder(json.JSONEncoder):
    def default(self, obj):
        if isinstance(obj, np.floating): return float(obj)
        if isinstance(obj, np.integer): return int(obj)
        if isinstance(obj, np.ndarray): return obj.tolist()
        return super().default(obj)

# check if image has a CRS
with rasterio.open(image) as src:
    has_crs = src.crs is not None
    transform = src.transform if has_crs else None
    crs = src.crs if has_crs else None
    print(f"CRS found: {crs}" if has_crs else "No CRS — using pixel coordinates")

def pixel_to_geo(row, col, transform):
    """Convert pixel (row, col) to geographic (lon, lat)"""
    lon, lat = xy(transform, row, col)
    return [lon, lat]

features = []
for pred in needed_predictions:
    if pred.mask is not None:
        mask = pred.mask.bool_mask
        contours, _ = cv2.findContours(
            mask.astype(np.uint8),
            cv2.RETR_EXTERNAL,
            cv2.CHAIN_APPROX_SIMPLE
        )
        for contour in contours:
            if len(contour) >= 3:
                points = contour.squeeze()
                if len(points.shape) == 1:
                    continue  # skip degenerate contours

                if has_crs:
                    # pixel (x=col, y=row) - geographic coords
                    coords = [pixel_to_geo(pt[1], pt[0], transform) for pt in points]
                else:
                    # flip y axis for pixel coords (image y is top-down, geojson is bottom-up)
                    img_height = pred.mask.bool_mask.shape[0]
                    coords = [[int(pt[0]), int(img_height - pt[1])] for pt in points]

                coords.append(coords[0])  # close polygon
                features.append({
                    "type": "Feature",
                    "geometry": {"type": "Polygon", "coordinates": [coords]},
                    "properties": {
                        "class": pred.category.name,
                        "confidence": float(pred.score.value)
                    }
                })

geojson_output = {
    "type": "FeatureCollection",
    "crs": {"type": "name", "properties": {"name": str(crs)}} if has_crs else None,
    "features": features
}

with open("output/boats.geojson", "w") as f:
    json.dump(geojson_output, f, cls=NumpyEncoder)

print(f"Exported {len(features)} boat polygons")

